def _apply_phrase_map(text: str) -> str:
    """按 length desc 应用词组替换，避免短词组先 hit 拆开长词组。"""
    items = sorted(PHRASE_MAP.items(), key=lambda kv: len(kv[0]), reverse=True)
    for src, dst in items:
        if src and src in text:
            text = text.replace(src, dst)
    return text


def _apply_char_map(text: str) -> str:
    """字符级替换，作为词组之后的兜底（不在 CHAR_MAP 里的字保持原样）。"""
    if not CHAR_MAP_v2:
        return text
    out_chars = []
    for ch in text:
        out_chars.append(CHAR_MAP_v2.get(ch, ch))
    return "".join(out_chars)


def _convert_value(value):
    """递归把 dict / list / str 里的所有字符串转换成繁体。"""
    if isinstance(value, str):
        return _apply_char_map(_apply_phrase_map(value))
    if isinstance(value, dict):
        return {k: _convert_value(v) for k, v in value.items()}
    if isinstance(value, list):
        return [_convert_value(v) for v in value]
    return value


def _inject_meta(d: dict) -> dict:
    """在顶层注入 ``_meta.translationNote`` 注明派生来源。

    把 ``_meta`` 放在顶层而不是 ``page._meta`` 是因为：
    1. ``_meta`` 不在已知 namespace 内（page / settings / status / ...），
       不会被 i18n.js 的 ``data-i18n`` 解析到。
    2. 后续 ``test_i18n_orphan_keys.py`` 的 namespace 校验会把它当作
       元数据（如果它产生 orphan 警告，再调整放置位置）。
    """
    meta = {
        "translationNote": (
            "Auto-derived from zh-CN.json by scripts/gen_zhtw_from_zhcn.py "
            "(feat-zhtw-locale §3.3 MVP). Quality is best-effort; native "
            "zh-TW review welcome via PR to refine specific keys."
        ),
        "derivedFrom": "zh-CN",
        "source": "scripts/gen_zhtw_from_zhcn.py",
    }
    out = {"_meta": meta}
    out.update(d)
    return out


def _load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def _dump_json(path: Path, obj: dict) -> None:
    text = json.dumps(obj, ensure_ascii=False, indent=2)
    path.write_text(text + "\n", encoding="utf-8")


def _generate() -> None:
    src = _load_json(ZH_CN_PATH)
    converted = _convert_value(src)
    converted_with_meta = _inject_meta(converted)
    _dump_json(ZH_TW_PATH, converted_with_meta)
    print(f"[OK] wrote {ZH_TW_PATH}")


def _check() -> int:
    """schema diff（不覆盖）：报告 zh-CN 中存在但 zh-TW 缺失的 key。"""
    if not ZH_TW_PATH.exists():
        print(
            f"[ERR] {ZH_TW_PATH} 不存在；先跑 ``python scripts/gen_zhtw_from_zhcn.py``"
        )
        return 1
    cn = _load_json(ZH_CN_PATH)
    tw = _load_json(ZH_TW_PATH)
    missing = []
    _diff_keys(cn, tw, prefix="", missing=missing)
    if missing:
        print("[WARN] 以下 zh-CN keys 在 zh-TW 中缺失：")
        for k in missing:
            print(f"  - {k}")
        return 1
    print("[OK] zh-TW.json schema 与 zh-CN.json 同构")
    return 0


def _diff_keys(a: dict, b: dict, prefix: str, missing: list[str]) -> None:
    for k, va in a.items():
        if k.startswith("_"):
            continue
        full = f"{prefix}.{k}" if prefix else k
        if k not in b:
            missing.append(full)
            continue
        vb = b[k]
        if isinstance(va, dict) and isinstance(vb, dict):
            _diff_keys(va, vb, full, missing)


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description="Generate zh-TW.json from zh-CN.json")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Schema diff only; do not overwrite zh-TW.json",
    )
    args = parser.parse_args(argv)
    if args.check:
        return _check()
    _generate()
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
